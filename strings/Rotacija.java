package strings;

public class Rotacija {
	
	private boolean bool;
	private int br;
	public boolean isBool() {
		return bool;
	}
	public void setBool(boolean bool) {
		this.bool = bool;
	}
	public int getBr() {
		return br;
	}
	public void setBr(int br) {
		this.br = br;
	}
	
	public String toString() {
		return "Rotacija [bool=" + bool + ", br=" + br + "]";
	}

	
	
}
